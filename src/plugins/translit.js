export default function translit(word) {
  var alphabet = {
    "а": "a",
    "б": "b",
    "в": "v",
    "г": "g",
    "д": "d",
    "е": "e",
    "ё": "yo",
    "ж": "zh",
    "з": "z",
    "и": "i",
    "й": "y",
    "к": "k",
    "л": "l",
    "м": "m",
    "н": "n",
    "о": "o",
    "п": "p",
    "р": "r",
    "с": "s",
    "т": "t",
    "у": "u",
    "ф": "f",
    "х": "h",
    "ц": "ts",
    "ч": "ch",
    "ш": "sh",
    "щ": "sch",
    "ъ": "",
    "ы": "y",
    "ь": "",
    "э": "e",
    "ю": "yu",
    "я": "ya"
  };
  var translitWord = '';
  for (var i = 0; i < word?.length ?? 0; i++) {
    var letter = word[i].toLowerCase();
    if (alphabet[letter]) {
      translitWord += alphabet[letter];
    } else {
      translitWord += letter;
    }
  }
  return translitWord;
}
